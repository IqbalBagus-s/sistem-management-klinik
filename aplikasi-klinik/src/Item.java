    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String idobat = Iblidobat.getText();
        String namaobat = txtnamaobat.getText();
        String deskripsiobat = txtdeskripsi.getText();
        String hargajual = txtsprice.getText();
        String hargabeli = txtbprice.getText();
        String jumlah = txtqty.getText();
        
        try {
            pst = con.prepareStatement("INSERT INTO obat(idobat, namaobat, deskripsi, hargajual, hargabeli, jumlah) VALUES(?,?,?,?,?,?)");
            pst.setString(1, idobat);
            pst.setString(2, namaobat);
            pst.setString(3, deskripsiobat);
            pst.setString(4, hargajual);
            pst.setString(5, hargabeli);
            pst.setString(6, jumlah);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Data Obat Telah Ditambahkan !!!");
            AutoID();
            txtnamaobat.setText("");
            txtdeskripsi.setText("");
            txtsprice.setText("");
            txtbprice.setText("");
            txtqty.setText("");
            txtnamaobat.requestFocus();
            Obat_table();
            
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

