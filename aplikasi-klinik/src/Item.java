    public void AutoID() {
        try {
            Statement s = con.createStatement();
            rs = s.executeQuery("SELECT MAX(idobat) FROM obat");
            rs.next();
            String maxIDObat = rs.getString("MAX(idobat)");
            
            if(maxIDObat == null) {
                Iblidobat.setText("IU001");
            } else {
                long id = Long.parseLong(maxIDObat.substring(2));
                id++;
                Iblidobat.setText("IU" + String.format("%03d", id));
            }
                
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void Obat_table() {
        try {
            pst = con.prepareStatement("SELECT * FROM obat");
            rs = pst.executeQuery();
            ResultSetMetaData Rsm = (ResultSetMetaData) rs.getMetaData();
            
            int c;
            c = Rsm.getColumnCount();
            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()) {
                Vector v2 = new Vector();
                
                for(int i = 1; i <= c; i++) {
                    v2.add(rs.getString("idobat"));
                    v2.add(rs.getString("namaobat"));
                    v2.add(rs.getString("deskripsi"));
                    v2.add(rs.getString("hargajual"));
                    v2.add(rs.getString("hargabeli"));
                    v2.add(rs.getString("jumlah"));

                }
                df.addRow(v2);
            }
            
        } catch (SQLException ex) { // Perbaikan penulisan variabel
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }

