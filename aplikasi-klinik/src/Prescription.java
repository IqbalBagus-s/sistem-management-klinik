    public void AutoID() {
        try {
            Statement s = con.createStatement();
            rs = s.executeQuery("SELECT MAX(rno) FROM resep");
            rs.next();
            String Resepno = rs.getString("MAX(rno)");
            
            if(rs.getString("MAX(rno)") == null) {
                txtrno.setText("RS001");
            } else {
                long id = Long.parseLong(rs.getString("MAX(rno)").substring(2));
                id++;
                txtrno.setText("RS" + String.format("%03d", id));
            }
                
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
