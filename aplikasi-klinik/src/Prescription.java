    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rno = txtrno.getText();
        String chno = txtchno.getText();
        String petype = ptype.getText();
        String desc = txtdes.getText();
        
        try {
            pst = con.prepareStatement("INSERT INTO resep(rno, chno, doctorname, ptype, description) VALUES(?,?,?,?,?)");
            pst.setString(1, rno);
            pst.setString(2, chno);
            pst.setString(3, newdocname);
            pst.setString(4, petype);
            pst.setString(5, desc);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Data Resep Telah Ditambahkan !!!");
            AutoID();
            txtchno.setText("");
            ptype.setText("");
            txtdes.setText("");
            txtrno.requestFocus();
            
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

