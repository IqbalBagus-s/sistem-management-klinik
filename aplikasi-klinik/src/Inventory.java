    private void txtrnameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtrnameKeyPressed
        // nama obat otomatis muncul
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String dcode = txtrname.getText();
            try {
                pst = con.prepareStatement("SELECT * FROM obat WHERE idobat = ?");
                pst.setString(1, dcode);
                rs = pst.executeQuery();

                if(rs.next() == false) {
                    JOptionPane.showMessageDialog(this, "Obat Tidak Ditemukan !!!");
                } else {
                    String dname = rs.getString("namaobat");
                    txtoname.setText(dname.trim());
                    txtqty.requestFocus();
                }
            }catch (SQLException ex) { // Perbaikan penulisan variabel
                Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_txtrnameKeyPressed

