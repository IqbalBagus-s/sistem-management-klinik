    public void Sales() {
        DateTimeFormatter daa = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        LocalDateTime now = LocalDateTime.now();
        String date = daa.format(now);

        String subtot = txtcost.getText();
        String pay = txtpay.getText();
        String bal = txtbal.getText();

        int lastinsertid = 0;

        String query = "INSERT INTO sales(date, subtotal, pay, balance) VALUES (?,?,?,?)";
        try {
            pst = con.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, date);
            pst.setString(2, subtot);
            pst.setString(3, pay);
            pst.setString(4, bal);

            pst.executeUpdate();
            rs = pst.getGeneratedKeys();

            if (rs.next()) {
                lastinsertid = rs.getInt(1);
            }

            String query1 = "INSERT INTO sale_product(sales_id, prod_id, sellprice, qty, total) VALUES (?,?,?,?,?)";
            pst = con.prepareStatement(query1);
            String pres_id;
            String item_id;
            String price;
            String qty;
            int total;

            for (int i = 0; i < jTable1.getRowCount(); i++) {
                pres_id = (String) jTable1.getValueAt(i, 0);
                item_id = (String) jTable1.getValueAt(i, 1);
                qty = jTable1.getValueAt(i, 3).toString();
                int qty1 = Integer.parseInt(qty);  // Mengkonversi qty ke integer
                price = jTable1.getValueAt(i, 4).toString();  // Mengambil price sebagai string
                total = Integer.parseInt(jTable1.getValueAt(i, 5).toString());  // Mengkonversi total ke integer

                pst.setInt(1, lastinsertid);
                pst.setString(2, item_id);
                pst.setString(3, price);  // Mengatur price sebagai string
                pst.setInt(4, qty1);  // Mengatur qty sebagai integer
                pst.setInt(5, total);  // Mengatur total sebagai integer
                pst.executeUpdate();
            }

            JOptionPane.showMessageDialog(this, "Data Tersimpan !!!");

        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (rs != null) rs.close();
                if (pst != null) pst.close();
            } catch (SQLException ex) {
                Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

