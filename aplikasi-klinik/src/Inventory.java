    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // fungsi tombol add
        String dcode = txtrname.getText();
        try {
            pst = con.prepareStatement("SELECT * FROM obat WHERE idobat = ?");
            pst.setString(1, dcode);
            rs = pst.executeQuery();
            while(rs.next()) {
                int currentqty;
                int sellprice;
                int qty;
                
                currentqty = rs.getInt("jumlah");
                sellprice = rs.getInt("hargajual");
                
                
                qty = Integer.parseInt(txtqty.getValue().toString());
                int tot = sellprice * qty;
                
                if(qty >= currentqty) {
                    JOptionPane.showMessageDialog(this, "Obat Yang Tersedia" + currentqty);
                    JOptionPane.showMessageDialog(this, "Jumlah Tidak Tersedia");
                }else {
                    DefaultTableModel dl = (DefaultTableModel) jTable1.getModel();
                    dl.addRow(new Object[]{
                        lblpid.getText(),
                        txtrname.getText(),
                        txtoname.getText(),
                        txtqty.getValue().toString(),
                        sellprice,
                        tot
                    });
                    int sum = 0;
                    for(int i = 0; i<jTable1.getRowCount(); i++){
                        sum = sum + Integer.parseInt(jTable1.getValueAt(i, 5).toString());
                    }
                    txtcost.setText(Integer.toString(sum));
                    txtrname.setText("");
                    txtoname.setText("");
                    txtqty.setValue(0);
                    txtrname.requestFocus();
                    

   
                }
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

